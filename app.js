var num = 100;

switch (num){

    case 100:
        console.log("Golden A+")
        break;

    case 80:
        console.log("A+")
        break;

    case 70:
        console.log("A")
        break;

    case 60:
        console.log("A-")
        break;

    case 50:
        console.log("B+")
        break;

    case 32:
        console.log("Fail")
        break;
        

}